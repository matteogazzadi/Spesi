export const translations: Record<LangCode, typeof en> = { en, it, es, fr, de, pt }

export function detectBrowserLang(): LangCode {
  const raw = navigator.language?.slice(0, 2).toLowerCase() ?? 'en'
  return (raw in translations ? raw : 'en') as LangCode
}

export function translate(lang: LangCode, key: string, vars?: Record<string, string | number>): string {
  const dict = translations[lang] ?? translations.en
  let str = (dict as Record<string, string>)[key] ?? (translations.en as Record<string, string>)[key] ?? key
  if (vars) {
    for (const [k, v] of Object.entries(vars)) {
      str = str.replace(`{{${k}}}`, String(v))
    }
  }
  return str
}
